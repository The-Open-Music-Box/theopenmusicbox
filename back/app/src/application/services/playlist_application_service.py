        if not playlist:
            logger.log(LogLevel.INFO, f"ðŸ·ï¸ No playlist found for NFC tag: {nfc_tag_id}")
            return None
        playlist_id = playlist.get("id")
        logger.log(LogLevel.INFO, f"âœ… Found playlist ID for NFC tag {nfc_tag_id}: {playlist_id}")
        return playlist_id

